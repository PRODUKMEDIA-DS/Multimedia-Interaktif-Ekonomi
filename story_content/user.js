function Script1()
{
  if (!window.bgMusic) {
    // TRIK SAKTI: Menambahkan angka unik (timestamp) di belakang nama file 
    // biar browser dipaksa download file baru dari GitHub, bukan dari cache.
    var versiBaru = "?v=" + new Date().getTime(); 
    
    window.bgMusic = new Audio("bgm.mp3" + versiBaru); 
    window.bgMusic.loop = true; 
    window.bgMusic.volume = 0.5; 
    
    // Jalankan musik
    window.bgMusic.play().then(function() {
        console.log("Autoplay sukses!");
    }).catch(function(error) {
        console.log("Autoplay diblokir. Menunggu klik pertama...");
        
        // Nunggu klik pertama di layar
        document.addEventListener('click', function playOnFirstClick() {
            window.bgMusic.play();
            document.removeEventListener('click', playOnFirstClick);
        }, { once: true });
    });
}
}

